// ???????????TaskID
void add_to_pool(int taskId) {
    // ???????????
    for (int i = 0; i < pool_size; i++) {
        if (task_pool[i] == taskId) {
            return; // ???????????????
        }
    }
    
    if (pool_size < MAX_TASK_SLOTS) {
        task_pool[pool_size++] = taskId;
    }
}

// ??????????TaskID
void remove_from_pool(int taskId) {
    for (int i = 0; i < pool_size; i++) {
        if (task_pool[i] == taskId) {
            // ?????????????
            for (int j = i; j < pool_size - 1; j++) {
                task_pool[j] = task_pool[j + 1];
            }
            pool_size--;
            break;
        }
    }
}

// ??????????????????TaskID
int select_random_from_pool() {
    if (pool_size > 0) {
        return task_pool[rand() % pool_size];
    }
    return 1; // ?????????????????
}

